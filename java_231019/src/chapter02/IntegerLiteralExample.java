package chapter02;

public class IntegerLiteralExample {
		public static void main(String[] args) {
			int var2 = 0b1011;
			int var8 = 0206;
			int var16 = 0x103FA ;
			int var = 15;
			System.out.println(var2);
			System.out.println(var8);
			System.out.println(var16);
			System.out.println(var);
			
}
}
